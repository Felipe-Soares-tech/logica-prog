// d) Elaborar um programa que leia 2 notas de um aluno em uma disciplina.
// Calcule e informe a média das notas.
// Exemplo:
// 1a Nota: 7.0
// 2a Nota: 8.0
// Média: 7.5

const nota1 = Number(prompt('qual foi sua primeira nota: '))
const nota2 = Number(prompt('qual foi sua segunda nota: '))
alert(`a media das suas notas é ${(nota1+nota2)/2}`)
